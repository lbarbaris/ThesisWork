    public void renderInterfaceBlock(Graphics2D g2, JComponent component, Player player, Gun gun) {
        int blockWidth = 280;
        int blockHeight = 80;
        int padding = 12;

        int blockX = (component.getWidth() - blockWidth) / 2;
        int blockY = component.getHeight() - blockHeight - 16;

        // ==== Fiery background ====
        GradientPaint gradient = new GradientPaint(blockX, blockY, new Color(90, 0, 0, 200), blockX + blockWidth, blockY + blockHeight, new Color(200, 50, 0, 200));
        g2.setPaint(gradient);
        g2.fillRoundRect(blockX, blockY, blockWidth, blockHeight, 20, 20);

        // ==== Glowing frame ====
        g2.setColor(new Color(25,5, 80, 0)); // Lava-bright outer
        g2.drawRoundRect(blockX - 1, blockY - 1, blockWidth + 2, blockHeight + 2, 22, 22);
        g2.setColor(new Color(15,0, 30, 0)); // Middle
        g2.drawRoundRect(blockX, blockY, blockWidth, blockHeight, 20, 20);
        g2.setColor(new Color(100, 0, 0)); // Inner
        g2.drawRoundRect(blockX + 2, blockY + 2, blockWidth - 4, blockHeight - 4, 16, 16);

        // ==== Glowing corner embers ====
        g2.setColor(new Color(25,5, 120, 0));
        g2.fillRect(blockX, blockY, 5, 5);
        g2.fillRect(blockX + blockWidth - 5, blockY, 5, 5);
        g2.fillRect(blockX, blockY + blockHeight - 5, 5, 5);
        g2.fillRect(blockX + blockWidth - 5, blockY + blockHeight - 5, 5, 5);

        // === Text styling ===
        g2.setFont(new Font("Monospaced", Font.BOLD, 16));

        // === HP box (blood red) ===
        String hpText = player.getHpString();
        g2.setColor(new Color(12,0, 0, 0));
        g2.fillRoundRect(blockX + padding, blockY + padding, 80, 24, 10, 10);
        g2.setColor(Color.BLACK);
        g2.drawRoundRect(blockX + padding, blockY + padding, 80, 24, 10, 10);
        g2.setColor(Color.WHITE);
        g2.drawString(hpText, blockX + padding + 8, blockY + padding + 17);

        renderUziArtCentered(g2, blockX + blockWidth / 2, blockY + blockHeight / 2 + 8);

        // === Ammo box (molten lava green-orange) ===
        String ammoText = gun.getCurrentAmmo() + " / " + gun.getMagazineSize();
        int ammoBoxX = blockX + blockWidth - padding - 80;
        g2.setColor(new Color(0, 90, 0));
        g2.fillRoundRect(ammoBoxX, blockY + padding, 80, 24, 10, 10);
        g2.setColor(Color.BLACK);
        g2.drawRoundRect(ammoBoxX, blockY + padding, 80, 24, 10, 10);
        g2.setColor(Color.WHITE);

        FontMetrics fm = g2.getFontMetrics();
        int textWidth = fm.stringWidth(ammoText);
        int ammoTextX = ammoBoxX + (80 - textWidth) / 2;
        int ammoTextY = blockY + padding + 17;
        g2.drawString(ammoText, ammoTextX, ammoTextY);

        // === Uzi pixel-art render ===

    }

    private void renderUziArtCentered(Graphics2D g2, int centerX, int centerY) {
        int artWidth = uziArt[0].length;
        int artHeight = uziArt.length;
        int scale = 1;

        int drawWidth = artWidth * scale;
        int drawHeight = artHeight * scale;

        int startX = centerX - drawWidth / 2;
        int startY = centerY - drawHeight / 2;

        for (int y = 0; y < artHeight; y++) {
            for (int x = 0; x < artWidth; x++) {
                int colorCode = uziArt[y][x];
                Color color = getColorFromCode(colorCode);
                if (color != null) {
                    g2.setColor(color);
                    g2.fillRect(startX + x * scale, startY + y * scale, scale, scale);
                }
            }
        }
    }

    public void renderReloadingBar(Graphics2D g2, Point playerPosition, int squareSize, Gun gun) {
        if (gun.isReloading()) {
            double progress = gun.getReloadProgress();
            int barHeight = 5;
            int barX = playerPosition.x;
            int barY = playerPosition.y - 10;

            g2.setColor(Color.DARK_GRAY);
            g2.fillRect(barX, barY, squareSize, barHeight);
            g2.setColor(Color.GREEN);
            g2.fillRect(barX, barY, (int) (squareSize * progress), barHeight);
        }
    }

    private Color getColorFromCode(int code) {
        return switch (code) {
            case 0 -> Color.BLACK;
            case 1 -> new Color(50, 50, 50);
            // Темно-серый
            case 2 -> new Color(100, 100, 100); // Серый
            case 3 -> new Color(150, 150, 150); // Светло-серый
            case 4 -> new Color(180, 180, 180); // Очень светлый серый
            default -> null;
        };
    }





